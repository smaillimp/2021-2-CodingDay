function weekNumbersToWords(input){
    String(input)
    let output = []
    switch(input) {
        case "1":
            output.push("Monday");
        case "2":
            output.push("Tuesday");
        case "3":
            output.push("Wednesday");
        case "4":
            output.push("Thursday");
        case "5":
            output.push("Friday");
        case "6":
            output.push("Saturday");
        case "7":
            output.push("Sunday!")
        
    }
    alert(output)
}
    

